    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        diaDelMes = Integer.valueOf(jTextField1.getText());
        //afirmacion = jCheckBox1.isSelected();
        if (diaDelMes > 0 && diaDelMes <= 31) {
            System.out.println("el dia del mes es = " + diaDelMes);
            if (jCheckBox1.isSelected()) {
                afirmacion = true;

                dias[diaDelMes] = true;
                System.out.println("Si");
            } else {
                afirmacion = false;
                System.out.println("No");
                dias[diaDelMes] = false;
            }
        } else {
            System.out.println("el numero tiene que ser entre 1 y 31");
            JOptionPane.showMessageDialog(this, "El numero debe ser entre 1 y 31");
        }
        jTextArea1.append(guardar.GuardarDia(diaDelMes, afirmacion));
    }//GEN-LAST:event_jButton1ActionPerformed

